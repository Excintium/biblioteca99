    private void cmbCategoriaCategoriaSeleccionada(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbCategoriaCategoriaSeleccionada
        if(evt.getStateChange() == evt.SELECTED){
            String idSeleccionadaCategoria = (String)evt.getItem();
            String[] partes = idSeleccionadaCategoria.split ("-");
            idSeleccionada = Integer.parseInt((partes[0]));
        }
    }//GEN-LAST:event_cmbCategoriaCategoriaSeleccionada

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Libro libroEncontrado = lc.buscarPorid(Integer.parseInt(txtidLibro.getText()));
        if(libroEncontrado != null){
            btnEditar.setEnabled(true);
            txtTitulo.setText(libroEncontrado.getTitulo());
            itemSeleccionado(libroEncontrado.getIdCategoria());
            idSeleccionada = libroEncontrado.getIdCategoria();
        }else{
            btnEditar.setEnabled(false);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

