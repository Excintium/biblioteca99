    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
       lc.editarLibro(Integer.parseInt(txtidLibro.getText()),txtTitulo.getText(), idSeleccionada); 
    }//GEN-LAST:event_btnEditarActionPerformed
    
    private void itemSeleccionado(int idCategoriaSeleccionada){
        for(int i = 0; i < cmbCategoria.getItemCount(); i++){
            String item = cmbCategoria.getItemAt(i); 
            int idCategoria = Integer.parseInt(item.split("-")[0]);
            if(idCategoria == idCategoriaSeleccionada){
                cmbCategoria.setSelectedIndex(i);
                break;
            }
        }
        
    }
    
    private void cargarCombo(){
        CategoriaController cc = new CategoriaController();
        for(Categoria i : cc.obtenerCategorias()){
            cmbCategoria.addItem(i.getId() + "-" + i.getNombre());
        }
    }
